
package PrakP2;

public class No4OperasiMatematika {
    public static void main(String[] args) {
        
        int X=22, Y=33, Jum=X+Y, Kur=X-Y, Kal=X*Y;
        double Bag=Y/X;
        System.out.println("Angka X=" + X + " Y=" + Y);
        System.out.println("Perkalian " + X + "x" + Y + "   = " +Kal);
        System.out.println("Pembagian " + Y + "/" + X + "   = " +Bag);
        System.out.println("Penjumlahan " + X + "+" + Y + " = " +Jum);
        System.out.println("Pengurangan " + X + "-" + Y + " = " +Kur);
    }
}
