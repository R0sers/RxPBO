
package PrakP2;


public class No1Increment {
    public static void main(String[] args) {
        
        int R = 10;
        System.out.println("Nilai awal R = " + R);
        
        //Increment
        R++;
        System.out.println("Increment --> R++ == Nilai R + 1");
        System.out.println("Nilai R setelah Increment = " + R);
              
    }
}
